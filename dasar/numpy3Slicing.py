import numpy as np

a = np.array([1, 2, 3, 4, 5])

# slicing

# mengambil index ke0 atau element ke 1
print("Element ke 1 = ", a[0])
print("Element Terakhir = ", a[-1])
print("Mengambil Element ke 2 sampai terakhir", a[1:])
print("Mengambil semua Element", a[:])
print("mengambil elemet ke 1 sampai 3", a[:3])